  <script>
    // Get the URL fragment (e.g., #filter-workforce)
    var urlFragment = window.location.hash;

    // Remove the leading '#'
    var filter = urlFragment.substring(1);

    // Hide all filters except the active one
    var filters = document.querySelectorAll("#portfolio-flters li");
    for (var i = 0; i < filters.length; i++) {
      var dataFilter = filters[i].getAttribute("data-filter");
      if (dataFilter === "." + filter) {
        filters[i].classList.add("filter-active");
      } else {
        filters[i].classList.remove("filter-active");
        filters[i].style.display = "none"; // Hide non-active filters
      }
    }

    // Show the corresponding content based on the active filter
    var portfolioItems = document.querySelectorAll(".portfolio-item");
    for (var i = 0; i < portfolioItems.length; i++) {
      var itemFilter = portfolioItems[i].classList.contains(filter);
      if (itemFilter) {
        portfolioItems[i].style.display = "block";
      } else {
        portfolioItems[i].style.display = "none";
      }
    }
  </script>



